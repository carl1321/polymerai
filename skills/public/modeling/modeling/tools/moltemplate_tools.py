        """
        从 Packmol 输出创建 LAMMPS 文件

        Args:
            packmol_output: Packmol 输出的 PDB 文件
            forcefield: 力场
            output_prefix: 输出前缀

        Returns:
            生成文件的路径

        TODO: 实现 Packmol -> Moltemplate 工作流
        """
